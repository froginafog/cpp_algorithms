//bubble sort
#include <stdio.h>

void swap(int * n1, int * n2);

int main()
{
    int a[] = {5, 3, 6, 1, 4, 2};
    int i;
    int num_items = sizeof(a)/sizeof(a[0]);

    printf("a = ");
    for(int k = 0; k < num_items; k++)
    {
        printf("  %d     ", a[k]);
    }
    printf("\n-------------------------------------------------\n");

    for(int n = 0; n < num_items; n++)
    {
        for(i = 0; i < num_items - 1; i++)
        {
            printf("i: %d\n", i);
            printf("a = ");
            for(int k = 0; k < num_items; k++)
            {
                if(k == i && i < num_items - 1)
                {
                    if(a[i] > a[i + 1])
                    {
                        printf(" (%d) <->", a[k]);
                    }
                    else
                    {
                        printf(" (%d)    ", a[k]);
                    }
                }
                else if(k == i + 1 && i < num_items - 1)
                {
                    printf(" (%d)    ", a[k]);
                }
                else
                {
                    printf("  %d     ", a[k]);
                }
            }
            printf("\n");

            if(a[i] > a[i + 1])
            {
                swap(&a[i], &a[i + 1]);
            }

            printf("a = ");
            for(int k = 0; k < num_items; k++)
            {
                printf("  %d     ", a[k]);
            }
            printf("\n-------------------------------------------------\n");
        }
    }

    printf("a = ");
    for(int k = 0; k < num_items; k++)
    {
        printf("  %d     ", a[k]);
    }
}

void swap(int * n1, int * n2)
{
    int temp = *n1;
    *n1 = *n2;
    *n2 = temp;
}

